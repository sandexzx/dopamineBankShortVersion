    echo "  $0 --rollback 20240330_120145  –û—Ç–∫–∞—Ç–∏—Ç—å—Å—è –∫ —É–∫–∞–∑–∞–Ω–Ω–æ–π –≤–µ—Ä—Å–∏–∏"
    echo ""
}

