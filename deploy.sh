# –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –≥–ª–∞–≤–Ω–æ–≥–æ —Ñ–∞–π–ª–∞ –¥–ª—è –∑–∞–ø—É—Å–∫–∞
find_main_file() {
    local dir=$1
    # –ò—â–µ–º —Ñ–∞–π–ª main.py –≤ –∫–æ—Ä–Ω–µ –ø—Ä–æ–µ–∫—Ç–∞
    if [ -f "$dir/main.py" ]; then
        echo "main.py"
        return 0
    fi
    
    # –ò—â–µ–º –¥—Ä—É–≥–∏–µ —Ñ–∞–π–ª—ã Python —Å –≤–æ–∑–º–æ–∂–Ω—ã–º –≤—ã–∑–æ–≤–æ–º Bot –∏–ª–∏ Dispatcher
    local main_candidates=$(find "$dir" -type f -name "*.py" -exec grep -l "import.*Bot\|Dispatcher" {} \;)
    
    if [ -n "$main_candidates" ]; then
        # –í—ã–±–∏—Ä–∞–µ–º –ø–µ—Ä–≤—ã–π –Ω–∞–π–¥–µ–Ω–Ω—ã–π —Ñ–∞–π–ª
        echo $(basename $(echo "$main_candidates" | head -n 1))
        return 0
    fi
    
    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É, –µ—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ
    echo ""
    return 1
}

