    log "INFO" "–ù–∞—Å—Ç—Ä–∞–∏–≤–∞—é —Ä–∞–∑—Ä–µ—à–µ–Ω–∏—è —Ñ–∞–π–ª–æ–≤"
    chown -R $SERVICE_USER:$SERVICE_USER "$INSTALL_DIR"
    check_result "–ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å —Ä–∞–∑—Ä–µ—à–µ–Ω–∏—è —Ñ–∞–π–ª–æ–≤" "warning"
    
    # –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º —Å–µ—Ä–≤–∏—Å
    log "INFO" "–ó–∞–ø—É—Å–∫–∞—é —Å–µ—Ä–≤–∏—Å $SERVICE_NAME"
