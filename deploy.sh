# –§—É–Ω–∫—Ü–∏–∏ –¥–ª—è –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
log() {
    local level=$1
    local message=$2
    local color=$NC
    local prefix=""

    case $level in
        "INFO")
            color=$GREEN
            prefix="[INFO]"
            ;;
        "WARNING")
            color=$YELLOW
            prefix="[WARNING]"
            ;;
        "ERROR")
            color=$RED
            prefix="[ERROR]"
            ;;
        "STEP")
            color=$BLUE
            prefix="[STEP]"
            ;;
    esac

    # –í—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å
    echo -e "${color}${prefix}${NC} $(date '+%Y-%m-%d %H:%M:%S') - $message"
    
    # –ó–∞–ø–∏—Å—å –≤ –ª–æ–≥-—Ñ–∞–π–ª
    echo "$(date '+%Y-%m-%d %H:%M:%S') - $prefix $message" >> $LOG_FILE
}

# –§—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —É—Å–ø–µ—à–Ω–æ—Å—Ç–∏ –∫–æ–º–∞–Ω–¥—ã
check_result() {
    if [ $? -ne 0 ]; then
        log "ERROR" "$1"
        if [ "$2" == "exit" ]; then
            exit 1
        fi
    fi
}

