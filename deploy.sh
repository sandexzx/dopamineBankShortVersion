    echo "  -h, --help            –ü–æ–∫–∞–∑–∞—Ç—å —ç—Ç—É —Å–ø—Ä–∞–≤–∫—É"
    echo ""
    echo "–ü—Ä–∏–º–µ—Ä—ã:"
    echo "  $0 --mode install     –í—ã–ø–æ–ª–Ω–∏—Ç—å –Ω–æ–≤—É—é —É—Å—Ç–∞–Ω–æ–≤–∫—É"
