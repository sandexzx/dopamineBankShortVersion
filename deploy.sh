    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏
    BACKUP_PATH="$BACKUP_DIR/$ROLLBACK_VERSION"
    if [ ! -d "$BACKUP_PATH" ]; then
        log "ERROR" "–†–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è $ROLLBACK_VERSION –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!"
        exit 1
    fi
    
    # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å–µ—Ä–≤–∏—Å
    log "INFO" "–û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é —Å–µ—Ä–≤–∏—Å $SERVICE_NAME"
    systemctl stop $SERVICE_NAME
    check_result "–ù–µ —É–¥–∞–ª–æ—Å—å –æ—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —Å–µ—Ä–≤–∏—Å $SERVICE_NAME" "warning"
    
    # –°–æ–∑–¥–∞–µ–º —Ä–µ–∑–µ—Ä–≤–Ω—É—é –∫–æ–ø–∏—é —Ç–µ–∫—É—â–µ–π –≤–µ—Ä—Å–∏–∏ –ø–µ—Ä–µ–¥ –æ—Ç–∫–∞—Ç–æ–º
    CURRENT_BACKUP_TIME=$(date '+%Y%m%d_%H%M%S')
    CURRENT_BACKUP_DIR="$BACKUP_DIR/pre_rollback_$CURRENT_BACKUP_TIME"
    
    if [ -d "$INSTALL_DIR" ]; then
        log "INFO" "–°–æ–∑–¥–∞—é —Ä–µ–∑–µ—Ä–≤–Ω—É—é –∫–æ–ø–∏—é —Ç–µ–∫—É—â–µ–π –≤–µ—Ä—Å–∏–∏ –ø–µ—Ä–µ–¥ –æ—Ç–∫–∞—Ç–æ–º: $CURRENT_BACKUP_DIR"
        mkdir -p "$CURRENT_BACKUP_DIR"
        cp -r "$INSTALL_DIR"/* "$CURRENT_BACKUP_DIR"/ 2>/dev/null
        check_result "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å —Ä–µ–∑–µ—Ä–≤–Ω—É—é –∫–æ–ø–∏—é —Ç–µ–∫—É—â–µ–π –≤–µ—Ä—Å–∏–∏" "warning"
    fi
    
    # –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∏–∑ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏
    log "INFO" "–í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é —Ñ–∞–π–ª—ã –∏–∑ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏ $ROLLBACK_VERSION"
    rm -rf "$INSTALL_DIR"/*
    cp -r "$BACKUP_PATH"/* "$INSTALL_DIR"/ 2>/dev/null
    check_result "–ù–µ —É–¥–∞–ª–æ—Å—å –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —Ñ–∞–π–ª—ã –∏–∑ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏" "exit"
    
